import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Route, Routes } from "react-router-dom";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { Toaster } from "@/components/ui/toaster";
import { TooltipProvider } from "@/components/ui/tooltip";
import Index from "./pages/Index.tsx";
import NotFound from "./pages/NotFound.tsx";
import ReportNeed from "./pages/ReportNeed.tsx";
import VolunteerRegister from "./pages/VolunteerRegister.tsx";
import Dashboard from "./pages/Dashboard.tsx";
import Matching from "./pages/Matching.tsx";

const queryClient = new QueryClient();

const App = () => (
  <QueryClientProvider client={queryClient}>
    <TooltipProvider>
      <Toaster />
      <Sonner />
      <BrowserRouter>
        <Routes>
          <Route path="/" element={<Index />} />
          <Route path="/report" element={<ReportNeed />} />
          <Route path="/volunteer" element={<VolunteerRegister />} />
          <Route path="/dashboard" element={<Dashboard />} />
          <Route path="/match/:id" element={<Matching />} />
          <Route path="*" element={<NotFound />} />
        </Routes>
      </BrowserRouter>
    </TooltipProvider>
  </QueryClientProvider>
);

export default App;
